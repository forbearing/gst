func init() {
	model.Register[*User]()
}
`,
			wantErr: false,
		},
		{
			name:         "user_group",
			pkgName:      "model",
			modelImports: []string{"helloworld/model"},
			stmts: []ast.Stmt{
				StmtModelRegister("User"),
				StmtModelRegister("Group"),
			},
			want: `// Code generated by gst; DO NOT EDIT.
